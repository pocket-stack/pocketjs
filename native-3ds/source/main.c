// PocketJS 3DS host — the 3DS analogue of native/src/main.rs (PSP) and
// host-web/engine.js (web). It boots QuickJS, exposes the `globalThis.ui` op
// surface (forwarding to the Rust bridge pocketjs-3ds-ffi), evaluates the same
// bundled Solid app, and per vblank ticks the core and presents its frame.
//
// ── SCAFFOLD ──────────────────────────────────────────────────────────────
// This file is complete and faithful to the proven dreamcart runtime-3ds host
// (same QuickJS boot, aptMainLoop frame loop, and 3DS→PSP button mapping) and
// to native/src/ffi.rs (the ui.* op set + __textures table), but it has NOT
// been compiled or run — the 3DS toolchain (devkitPro) is not installed in the
// authoring environment. Build + run it per native-3ds/README.md. Two spots
// need on-device confirmation and are marked "VERIFY ON DEVICE".
//
// v1 render path = present pj_render()'s software-rasterized RGBA framebuffer
// directly to the top screen (libctru gfx, no citro). With the "3ds" device
// profile the core lays out natively at 400×240, so the framebuffer maps 1:1 to
// the top screen — no scaling. A GPU-native DrawList→citro3d backend (mirroring
// native/src/ge.rs) is the documented v2; see README.md.

#include <3ds.h>
#include <stdio.h>
#include <string.h>

#include "quickjs.h"
#include "pocketjs_ffi.h"
#include "game_js.h"   // generated by gen-game.ts: GAME_JS[], GAME_JS_LEN
#include "game_pak.h"  // generated by gen-game.ts: GAME_PAK[], GAME_PAK_LEN

// libctru weak symbols (override the crt0 defaults).
//
// __stacksize__: the 3dsx main-thread stack defaults to ~32 KB, which QuickJS
// overflows while parsing/compiling a large bundle (its recursive-descent
// parser is deeply nested) — the exact reason native/src/main.rs runs QuickJS on
// a 2 MB PSP worker thread. Give the main thread the same 2 MB here (a stack
// overflow shows up as register spills writing below the stack, then PC→0).
u32 __stacksize__ = 2 * 1024 * 1024;

// FCRAM split: a cached general heap (newlib malloc — where the QuickJS C
// allocator lives) + a GPU-visible linear heap. Bias toward the general heap
// (v1 renders via gfx, whose framebuffers live in VRAM, and uses no GPU
// vertex/texture buffers, so linear can be small). Tune per app; probe at
// runtime with linearSpaceFree(). See native-3ds/README.md "Memory".
u32 __ctru_heap_size        = 40 * 1024 * 1024;
u32 __ctru_linear_heap_size = 12 * 1024 * 1024;

// QuickJS (the dreamcart fork) calls debug_log() during init; provide it.
void debug_log(const char *msg, int a) { (void)a; printf("[qjs] %s\n", msg); }

// ── arg helpers ────────────────────────────────────────────────────────────

static int32_t arg_i32(JSContext *ctx, int argc, JSValueConst *argv, int i) {
    int32_t v = 0;
    if (i < argc) JS_ToInt32(ctx, &v, argv[i]);
    return v;
}
static double arg_f64(JSContext *ctx, int argc, JSValueConst *argv, int i) {
    double v = 0.0;
    if (i < argc) JS_ToFloat64(ctx, &v, argv[i]);
    return v;
}

// Borrow the bytes behind an ArrayBuffer OR a typed-array view (host.ts passes
// Uint8Arrays), mirroring native/src/ffi.rs buffer_bytes. Pointer is valid only
// until the next JS allocation — consume it before returning to JS.
static uint8_t *buffer_bytes(JSContext *ctx, JSValueConst val, size_t *out_len) {
    size_t len = 0;
    uint8_t *p = JS_GetArrayBuffer(ctx, &len, val);
    if (p) { *out_len = len; return p; }
    JS_FreeValue(ctx, JS_GetException(ctx)); // clear the pending TypeError
    JSValue buf = JS_GetPropertyStr(ctx, val, "buffer");
    size_t blen = 0;
    uint8_t *bp = JS_GetArrayBuffer(ctx, &blen, buf);
    JS_FreeValue(ctx, buf);
    if (!bp) { JS_FreeValue(ctx, JS_GetException(ctx)); return NULL; }
    int32_t off = 0, vlen = 0;
    JSValue ov = JS_GetPropertyStr(ctx, val, "byteOffset");
    JS_ToInt32(ctx, &off, ov); JS_FreeValue(ctx, ov);
    JSValue lv = JS_GetPropertyStr(ctx, val, "byteLength");
    JS_ToInt32(ctx, &vlen, lv); JS_FreeValue(ctx, lv);
    if (off < 0 || vlen < 0 || (size_t)(off + vlen) > blen) return NULL;
    *out_len = (size_t)vlen;
    return bp + off;
}

