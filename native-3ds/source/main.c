static JSValue ui_createNode(JSContext *ctx, JSValueConst t, int argc, JSValueConst *argv) {
    (void)t; return JS_NewInt32(ctx, pj_create_node((uint32_t)arg_i32(ctx, argc, argv, 0)));
}
