// static/compiler/targets/gba.ts — the GBA packager: native 4bpp art, one
// generated gen_data.c (flat ROM keeps every blob addressable), and the
// arm-none-eabi toolchain drive + cartridge header patch.

import { $ } from "bun";
import { dirname, join } from "node:path";
import { FONT_GLYPHS, TARGETS, bgLayout } from "../../spec/isa.ts";
import { SPRITE_ENTRY_SIZE } from "../../spec/rpg.ts";
import {
  encodeTileGba,
  frameToTiles,
  gbaPaletteBank,
  glyphTile,
  tileFromHexRows,
} from "../assets.ts";
import { FONT8 } from "../font.gen.ts";
import type { LinkedGame } from "../link.ts";

const RUNTIME = join(import.meta.dir, "..", "..", "runtime");

// GBA text ink/paper pixel values inside font tiles (palette bank 15).
const INK = 1;
const PAPER = 2;

export interface GbaArtifacts {
  genC: string;
}

export function emitGbaData(linked: LinkedGame): string {
  const { model } = linked;
  const t = TARGETS.gba;
  const L = bgLayout(t);
  const lines: string[] = [];
  const push = (s: string) => lines.push(s);

  const cArray = (name: string, bytes: ArrayLike<number>, type = "unsigned char"): void => {
    const vals: string[] = [];
    for (let i = 0; i < bytes.length; i++) vals.push(String(bytes[i]));
    push(`const ${type} ${name}[] = { ${vals.join(",")} };`);
  };

  push("/* gen_data.c — GENERATED by targets/gba.ts. DO NOT EDIT. */");
  push('#include "spec_gen.h"');
  push("typedef unsigned char u8; typedef unsigned short u16;");

  // Fixed-region tables.
  cArray("ps_game_header", linked.header, "u8");
  {
    const vals = linked.scriptTable.map(String).join(",");
    push(`const u16 ps_script_table[] = { ${vals} };`);
  }
  {
    const b: number[] = [];
    for (const e of linked.textTable) b.push(e.blob, e.off & 0xff, (e.off >> 8) & 0xff);
    cArray("ps_text_table", b, "u8");
  }
  cArray("ps_map_blob", linked.mapBlobIndex, "u8");

  // Blobs.
  linked.blobs.forEach((b, i) => cArray(`ps_blob_${i}`, b.bytes, "u8"));
  push(`const u8 *const ps_blobs[] = { ${linked.blobs.map((_, i) => `ps_blob_${i}`).join(",")} };`);

  // BG art tiles (index 1..N; blank is index 0 and stays zero-filled).
  const art: number[] = [];
  for (let i = 1; i < model.tileNames.length; i++) {
    const tile = model.tileset.tiles[model.tileNames[i]];
    art.push(...encodeTileGba(tileFromHexRows(tile.px)));
  }
  cArray("ps_bg_art", art, "u8");
  push(`const u16 ps_bg_art_tiles = ${model.tileNames.length - 1};`);

  // Font tiles (95 glyphs at the fixed base).
  const font: number[] = [];
  for (let g = 0; g < FONT_GLYPHS; g++) {
    font.push(...encodeTileGba(glyphTile(FONT8[g], INK, PAPER)));
  }
  cArray("ps_font", font, "u8");

  // OBJ tiles + sprite table. GBA 1D mapping: frame block = TL,TR,BL,BR.
  const objBytes: number[] = [];
  const spriteTable: number[] = [];
  let tileBase = 0;
  model.sprites.forEach((sp, si) => {
    const framesPerDir = sp.facings.down.length;
    spriteTable.push(tileBase & 0xff, (tileBase >> 8) & 0xff, framesPerDir, si);
    for (const facing of ["down", "up", "right"] as const) {
      for (const frame of sp.facings[facing]) {
        for (const tile of frameToTiles(frame)) objBytes.push(...encodeTileGba(tile));
        tileBase += 4;
      }
    }
  });
  if (spriteTable.length !== model.sprites.length * SPRITE_ENTRY_SIZE) {
    throw new Error("sprite table drifted");
  }
  cArray("ps_obj_tiles", objBytes, "u8");
  push(`const u16 ps_obj_tile_count = ${tileBase};`);
  cArray("ps_sprite_table", spriteTable, "u8");
  push(`const u8 ps_sprite_count = ${model.sprites.length};`);

  // Palettes: BG bank 0 = tileset, BG bank 15 = textbox; OBJ bank i = sprite i.
  cArray("ps_bg_pal0", Array.from(gbaPaletteBank(model.tileset.palette)), "u16");
  const textPal = new Uint16Array(16);
  textPal[INK] = 0x0000; // black ink
  textPal[PAPER] = 0x7fff; // white paper
  cArray("ps_text_pal", Array.from(textPal), "u16");
  {
    const objPal: number[] = [];
    for (const sp of model.sprites) objPal.push(...gbaPaletteBank(sp.palette));
    cArray("ps_obj_pal", objPal, "u16");
  }
  push(`const u16 ps_bg_font_base = ${L.fontBase};`);

  return lines.join("\n") + "\n";
}

