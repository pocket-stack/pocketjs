export async function buildGba(linked: LinkedGame, outRom: string): Promise<void> {
  const outDir = dirname(outRom);
  const genDir = join(outDir, "gen-gba");
  await $`mkdir -p ${genDir}`.quiet();
  await Bun.write(join(genDir, "gen_data.c"), emitGbaData(linked));

  const gbaDir = join(RUNTIME, "gba");
  const coreDir = join(RUNTIME, "core");
  const genInc = join(RUNTIME, "gen");
  const elf = join(genDir, "game.elf");

  await $`arm-none-eabi-gcc -mcpu=arm7tdmi -mthumb-interwork -marm -ffreestanding -nostdlib -O2 -fno-strict-aliasing -Wall -Werror=implicit-function-declaration -DPS_TARGET_GBA -I${genInc} -I${coreDir} -I${gbaDir} -T${gbaDir}/gba.ld ${gbaDir}/crt0.s ${coreDir}/vm.c ${coreDir}/rpg.c ${gbaDir}/hal_gba.c ${join(genDir, "gen_data.c")} -lgcc -o ${elf}`;
  await $`arm-none-eabi-objcopy -O binary ${elf} ${outRom}`;

  // Cartridge header: logo + title + checksum (flashcart-friendly).
  const rom = new Uint8Array(await Bun.file(outRom).arrayBuffer());
  rom.set(NINTENDO_LOGO, 0x04);
  const title = linked.model.title.toUpperCase().replace(/[^A-Z0-9 ]/g, "").slice(0, 12);
  for (let i = 0; i < 12; i++) rom[0xa0 + i] = i < title.length ? title.charCodeAt(i) : 0;
  rom.set([0x50, 0x53, 0x54, 0x43], 0xac); // game code "PSTC"
  rom[0xb2] = 0x96; // fixed value
  let sum = 0;
  for (let i = 0xa0; i <= 0xbc; i++) sum = (sum + rom[i]) & 0xff;
  rom[0xbd] = (-(0x19 + sum)) & 0xff;
  await Bun.write(outRom, rom);
}
